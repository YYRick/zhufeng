/* 打印100以内的数，除了可以被7整除的或者个位数是7的数 */
for(var i = 0;i < 100;i++){
    /* 
     i % 7 == 0 可以被7整除的数
     i % 10 == 7 个位数是7的数
    */
   if(i % 7 == 0 || i % 10 == 7){
       /* 此处不填就相当于continue */
   }else{
       console.log(i);
   }
}

//可以被4,5,6整除的数
for(var i = 0; i <= 100; i++){
    if(i % 4 == 0 || i % 5 == 0 || i % 6 == 0){
        console.log(i);
    }
}